<template>
  <div class="login-form">
    <div class="g-form">
      <div class="g-form-line">
        <span class="g-form-lable">用户名：</span>
        <div class="g-form-input">
          <input type="text" placeholder="请输入用户名" v-model="usernameModel">
        </div>
        <span class="error">{{userErrors.errorText}}</span>
      </div>
      <div class="g-form-line">
        <span class="g-form-lable">密码：</span>
        <div class="g-form-input">
          <input type="password" v-model="passwordModel" placeholder="请输入密码">
        </div>
         <span class="error">{{passErrors.errorText}}</span>
      </div> 
      <div class="g-form-line">
        <div class="g-form-btn">
          <a class="button" @click="onLogin">登录</a>
        </div>
      </div> 
      <p>{{errorText}}</p>        
    </div>
  </div>
</template>

<script>
export default({
  data(){
    return{
      usernameModel:'',
      passwordModel:'',
      errorText: ''
    }
  },
  computed:{
    userErrors(){
      let errorText,status
     if(!/@/g.test(this.usernameModel)){
        status=false
        errorText="不包含@"
     }else{
        status=true
        errorText=""        
      }
      if(!this.userFlag){
        errorText=""
        this.userFlag=true
      }
      return{
        status,
        errorText
      }
    },
    passErrors(){
    let errorText,status
     if(!/^\w{1,6}$/g.test(this.passwordModel)){
        status=false
        errorText="密码在1-6位数"
     }else{
        status=true
        errorText=""        
      }
      if(!this.passFlag){
        errorText=''
        this.passFlag=true
      }      
      return{
        status,
        errorText
      }      
    }
  },
  methods:{
    onLogin(){
      if(!this.userErrors.status||!this.passErrors.status){
        this.errorText='部分选项未通过'
      } else{
        this.errorText=''
        this.$http.get('/api/loging').then(res=>{
          res=res.body
          let datas=res.data
          this.$emit('has-log',datas)
          
        },error=>{
          console.log('error')
        })
      }
    }
  }
})
</script>


